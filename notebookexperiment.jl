Markdown.parse("""We start by applying the Inexact Extragradient Method (without line search) to a problem presented in <https://arxiv.org/pdf/1411.4338.pdf>.

This problem solves a VIP with operator $(@latexrun T(x) = [-1 -1;1 -1]*x + [3/2;1/2]) over a unit ball.

Unlike in the original papers we set the feasible set to be the ball over the norm ``\\|⋅\\|_{10}``, that is, 
``` math
C = \\{(x,y): x^{10}+y^{10}≤ 1\\}.
```""")

# ╔═╡ 6ad81dab-94e4-4c45-9245-d92a39c87432
C = Ballp(10.,zeros(2),1.0);

