md"## Inexact Extragradient Method with Line Search"

# ╔═╡ e822821c-3c6b-4947-9693-9bc792477ccf
Markdown.parse("""We now apply the Inexact Extragradient Method with Line Search to a VIP problem where the operator is not Lipschitz. Namely the operator is defined using $(latexify(:(t = x[1]+sqrt(x[1]^2+4*x[2])/2),index=:subscript)), and 
$(@latexify T(x)=[-t/(1+t),-t/(1+t)]).


This test problem is taken from <https://arxiv.org/pdf/1609.09569.pdf>. 

As before we change the feasible set to be the ``\\|⋅\\|_{10}`` ball.""")

# ╔═╡ 0a87e398-6e88-402d-b0f6-0a04f62cc116
function T2(x)
	t = x[1]+√(x[1]^2+4x[2])/2
	return [-t/(1+t),-t/(1+t)]
end

# ╔═╡ c622c301-7bf3-446d-af8e-bda994726a26
pb4 = VIP(Ballp(10.,zeros(2),1.0),T2)

# ╔═╡ fdb03d24-d05c-4c12-ab70-6c9c26fb4b31
begin
	β = 0.9
	s = 0.99
	α = 0.9
	ρ = √3-1
end;

# ╔═╡ fc8d8ccc-e3e5-4c56-a43a-6e2c2932d559
m1 = InexactExtragradient(pb4,β,s,ρ,α);

# ╔═╡ 3e0c09b8-0f4f-4ecc-b413-2e328444a805
yy = collect(take(m1,1000));

# ╔═╡ bdc6a44d-e75b-49cf-8c85-d261952e228e
begin
p2 = Plots.plot(pb4.C,alpha=0.2,ratio=1,color=:gray,xlims=[0,1],ylims=[0.9,1])
	Plots.plot!([x[1] for x in yy],[x[2] for x in yy],marker=:circle,ms=4,label="iterates")
	Plots.plot!(title=f"Results for α=\(α), ρ≈ \%.2f(ρ), β=\(β), σ=\(s)",size=(1000,200),legend=:bottomleft)
end

